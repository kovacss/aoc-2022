
// X -> Rock
// Y -> Paper
// Z -> Scissors
type Item = 'X' | 'Y' | 'Z' | 'A' | 'B' | 'C';

function scoreFromShapePart1(us: Item) {
    switch (us) {
        case 'X':
        case 'A':
            return 1;
        case 'Y':
        case 'B':
            return 2;
        case 'Z':
        case 'C':
            return 3;
    }
}

function scoreFromShapePart2(strategy: Item, toCompare: Item) {
    if (!toCompare) {
        return scoreFromShapePart1(strategy);
    }

    const opponent = scoreFromShapePart1(toCompare);
    switch (strategy) {
        case 'Y':
            // Same as opponent
            return opponent;
        case 'Z':
            return (opponent + 1) > 3 ? 1 : opponent + 1;
        case 'X':
            return (opponent - 1) == 0 ? 3 : opponent - 1;
    }
}



function play(opponent: Item, us: Item, map: (arg1: Item, arg2?: Item) => number) {
    const ourScore = map(us, opponent);
    const opponentScore = map(opponent);

    const output = ourScore - opponentScore;
    if (output == 0) {
        return 3 + ourScore;
    }

    if (output == 1 || output == -2) {
        return 6 + ourScore;
    }

    return ourScore;
}

