function countStacks(line: string) {
    const length = line.length;
    return Math.floor(length / 4) + 1;
}

function buildStacks(input: string) {
    let stackNumbers = countStacks(input.split("\n")[0])
    const stacks: string[][] = [];
    while (stackNumbers-- > 0) {
        stacks.push([]);
    }
    console.log("Stack length", stacks.length);

    return stacks;
}

function fillStacks(stacks: string[][], input: string) {
    const lines = input.split("\n");
    let done = false;
    lines.forEach(element => {
        let e = element.indexOf("[");
        if (done || e == -1) {
            done = true;
            return;
        }

        while (e != -1) {
            // console.log(index, e, element)
            // Between the "[" and "]"
            const character = element.substring(e + 1, e + 2);
            const index = e / 4;
            // console.log(index + 1)
            // console.log(character);
            e = element.indexOf("[", e + 1);

            stacks[index].push(character)
        }
    });
}

