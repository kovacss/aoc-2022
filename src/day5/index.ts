function sparseInstruction(inst: string): [any, any, any] {
    const start = Number.parseInt(inst.substring(inst.indexOf("from ") + 5, inst.indexOf(" to")));
    const end = Number.parseInt(inst.substring(inst.indexOf("to ") + 3));
    let numberElementsToMove = Number.parseInt(inst.substring(inst.indexOf("move ") + 5, inst.indexOf(" from")));

    return [numberElementsToMove, start, end];
}
